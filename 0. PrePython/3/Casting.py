#1. 데이터를 문자(열)로 형변환
iNum = 10
fNum = 3.14
flag = True

print(iNum)
print(type(iNum))

print(fNum)
print(type(fNum))

print(flag)
print(type(flag))

iNum = str(iNum)
fNum = str(fNum)
flag = str(flag)

print(iNum)
print(type(iNum))

print(fNum)
print(type(fNum))

print(flag)
print(type(flag))

